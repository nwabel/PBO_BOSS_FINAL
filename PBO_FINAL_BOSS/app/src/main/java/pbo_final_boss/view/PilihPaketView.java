    public PilihPaketView(Mahasiswa mhs) {
        org.hibernate.Session session = HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        session.refresh(mhs);
        this.mhs = mhs;
        initComponents();
        MyController c = new MyController();
        List<PaketSoal> ps = c.isiListPaket();
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for(PaketSoal p : ps){
            dlm.addElement(p.getTypePaket());
        }
        DefaultListModel<String> dlmm = new DefaultListModel<>();
        for(PaketSoal s : mhs.daftarPaketPilihan){
            dlmm.addElement(s.getTypePaket());
        }
        jList2.setModel((ListModel)dlmm);
        jList1.setModel((ListModel)dlm);
        jButton1.addActionListener(this);
        jButton2.addActionListener(this);
        jButton3.addActionListener(this);
        session.getTransaction().commit();
        session.close();
    }

