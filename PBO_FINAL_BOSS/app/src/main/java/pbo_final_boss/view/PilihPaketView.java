    @Override
    public void actionPerformed(ActionEvent e) {
        MyController c = new MyController();
        if(e.getSource() == jButton1){
            String s = jList1.getSelectedValue();
            c.selectedPaketSoal(mhs, s);
            JOptionPane.showMessageDialog(null, "Paket Berhasil Ditambahkan ke Daftar Anda");
            this.setVisible(false);
            PilihPaketView ps = new PilihPaketView(mhs);
            ps.setVisible(true);
        }else if(e.getSource() == jButton2){
            SoalView sv = new SoalView(mhs);
            sv.setVisible(true);
            this.setVisible(false);
        }else if(e.getSource() == jButton3){
            this.setVisible(false);
            DashboardMhsView dmv = new DashboardMhsView(mhs);
            dmv.setVisible(true);
        }
    }
